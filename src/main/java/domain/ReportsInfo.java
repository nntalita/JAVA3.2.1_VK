package domain;

public class ReportsInfo {
    private int count;
    private int userReposted;

    public int getCountReportsInfo() {
        return count;
    }

    public void setCountReportsInfo(int count) {
        this.count = count;
    }

    public int getUserReposted() {
        return userReposted;
    }

    public void setUserReposted(int userReposted) {
        this.userReposted = userReposted;
    }
}
